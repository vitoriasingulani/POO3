// Faça um programa que imprima a tabuada do número 7.

import java.util.*;

public class ExeCinco {
    public static void main(String[] args) {
        Scanner scanner = new Scanner(System.in);

        for (int i = 1; i <= 10; i++) {
            System.out.println("7 x " + i + " = " + (7 * i));
        }
    }
}

